##Crie um programa que leia dois numeros e mostre a soma entre eles.

n1 = float(input('Digite um numero: '))
n2 = float(input('Digite outro numero: '))
soma = n1 + n2
print('A soma de {} + {} = {}'.format(n1,n2,soma))
#Pode usar a soma de duas formas :1° Com uma variavel ex(soma)
# 2° Somando com operador + ex (n1 + n2)